package com.ss.jb.one;

/** Printing Patterns
 * @author Christopher O'Neill
 *
 */
public class Assignment1 {

	/** Since the patterns are deterministic
	 * (No inputs or anything that make them variable length)
	 * I've opted to hard code the pattern printing for efficiency
	 * purposes.
	 * @param args
	 */
	public static void main(String[] args) {
		
		//Pattern 1
		System.out.println("1)");
		System.out.println("*");
		System.out.println("**");
		System.out.println("***");
		System.out.println("****");
		System.out.println(".........");
		
		//Pattern 2
		System.out.println("2)");
		System.out.println(".........");
		System.out.println("****");
		System.out.println("***");
		System.out.println("**");
		System.out.println("*");

		//Pattern 3
		System.out.println("3)");
		System.out.println("     *");
		System.out.println("    ***");
		System.out.println("   *****");
		System.out.println("  *******");
		System.out.println("...........");
		
		//Pattern 4
		System.out.println("4)");
		System.out.println("...........");
		System.out.println("  *******");
		System.out.println("   *****");
		System.out.println("    ***");
		System.out.println("     *");
		
	}

}
